fn main() {
    /* 
    `i8` is the smallest byte. This means I've bound the size of the value of `x`
     I also added the value
     Note: Here, the bound is the type given to number values
      */
    let x: i8 = 10;
    if x == 10 {
        println!("x is ten!");
    } else {
        println!("x is not ten!");
    }
}
